export const somar = (num1, num2) => {
    return num1 + num2;
}

export const salario = (valorHora, horasTrabalhadas) => {
    return valorHora * horasTrabalhadas;
}

export const mediaPesos = (peso1, peso2, peso3, peso4, peso5) => {
    return (peso1 + peso2 + peso3 + peso4 + peso5) / 5;
}

export const conversao = (celsius) => {
    return (9 * celsius + 160) / 5;
}

export const km = (milhas) => {
    return milhas * 1.60934;
}