export const somarPost = (num1, num2) => {
    return num1 + num2
}

export const salarioPost = (valorHora, horasTrabalhadas) => {
    return valorHora * horasTrabalhadas
}

export const mediaPesosPost = (peso1, peso2, peso3, peso4, peso5) => {
    return (peso1 + peso2 + peso3 + peso4 + peso5) / 5;
}

export const conversaoPost = (celsius) => {
    return (9 * celsius + 160) / 5;
}

export const kmPost = (milhas) => {
    return milhas * 1.60934;
}